#include <ft_ssl.h>

const uint32_t g_exp[48] = {
	32, 1, 2, 3, 4, 5, 4, 5, 6, 7,
	8, 9, 8, 9, 10, 11, 12, 13, 12,
	13, 14, 15, 16, 17, 16, 17, 18,
	19, 20, 21, 20, 21, 22, 23, 24,
	25, 24, 25, 26, 27, 28, 29, 28,
	29, 30, 31, 32, 1
};

void	function_extension(uint32_t right, uint64_t *ext)
{
	int	i;

	i = 0;
	*ext = 0;
	while (i < 48)
	{
		if ((right & (FIRST_BIT_32 >> (g_exp[i] - 1))) != 0)
			*ext += (FIRST_BIT_64 >> (i + 16));
		i++;
	}
}
