#include <ft_ssl.h>

const uint32_t g_sp[32] = {
	16, 7, 20, 21, 29, 12, 28, 17, 1,
	15, 23, 26, 5, 18, 31, 10, 2, 8,
	24, 14, 32, 27, 3, 9, 19, 13, 30,
	6, 22, 11, 4, 25
};

void		second_permutation(uint32_t sbox, uint32_t *trans2)
{
	int		i;

	i = 0;
	*trans2 = 0;
	while (i < 32)
	{
		if (((sbox << (g_sp[i] - 1)) & FIRST_BIT_32) != 0)
			*trans2 += (FIRST_BIT_32 >> i);
		i++;
	}
}
