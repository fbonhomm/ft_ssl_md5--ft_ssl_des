#include <ft_ssl.h>

const uint32_t g_pc2[48] = {
	14, 17, 11, 24, 1, 5,
	3, 28, 15, 6, 21, 10,
	23, 19, 12, 4, 26, 8,
	16, 7, 27, 20, 13, 2,
	41, 52, 31, 37, 47, 55,
	30, 40, 51, 45, 33, 48,
	44, 49, 39, 56, 34, 53,
	46, 42, 50, 36, 29, 32
};

void			permutation_pc2(uint64_t *arr)
{
	size_t		j;
	size_t		i;
	uint64_t	tmp;

	i = 0;
	while (i < 16)
	{
		tmp = 0;
		j = 0;
		while (j < 48)
		{
			if (((arr[i] << (g_pc2[j] - 1)) & FIRST_BIT_64) != 0)
				tmp += (FIRST_BIT_64 >> j);
			j++;
		}
		arr[i] = tmp;
		i++;
	}
}
