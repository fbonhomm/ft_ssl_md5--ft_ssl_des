#include <ft_ssl.h>

const uint32_t g_pc1[56] = {
	57, 49, 41, 33, 25, 17, 9,
	1, 58, 50, 42, 34, 26, 18,
	10, 2, 59, 51, 43, 35, 27,
	19, 11, 3, 60, 52, 44, 36,
	63, 55, 47, 39, 31, 23, 15,
	7, 62, 54, 46, 38, 30, 22,
	14, 6, 61, 53, 45, 37, 29,
	21, 13, 5, 28, 20, 12, 4
};

void			key_64_to_56(uint64_t key, uint32_t *left, uint32_t *right)
{
	int			i;
	uint64_t	tmp;

	i = 0;
	tmp = 0;
	*left = 0;
	*right = 0;
	while (i < 56)
	{
		if (((key << (g_pc1[i] - 1)) & FIRST_BIT_64) != 0)
			tmp += (FIRST_BIT_64 >> i);
		i++;
	}
	i = 0;
	while (i < 28)
	{
		if (((tmp << i) & FIRST_BIT_64) != 0)
			*left += (FIRST_BIT_28 >> i);
		if (((tmp << (i + 28)) & FIRST_BIT_64) != 0)
			*right += (FIRST_BIT_28 >> i);
		i++;
	}
}
