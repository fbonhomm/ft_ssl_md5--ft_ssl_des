#include "libft.h"

void		ft_bit_to_str(uint8_t *ptr, char *str)
{
	int		i;
	size_t	j;
	uint8_t	ch;

	ch = *ptr;
	i = 7;
	j = 0;
	while (i >= 0)
		str[j++] = (ch & (1 << i--)) ? '1' : '0';
}
